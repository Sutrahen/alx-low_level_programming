#include "header.h"

/**
 * add - report sum of two numbers
 * @a: first number
 * @b: second number
 * Return: sum of two nuumbers
 */

int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - report difference of two numbers
 * @a: first number
 * @b: second number
 * Return: difference of two numbers
 */

int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - report product of two numbers
 * @a: first number
 * @b: second number
 * Return: Product of two numbers
 */

int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - report quotient of two numbers
 * @a: first number
 * @b: second number
 * Return: quotient of two numbers
 */

int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - report remainder of two numbers
 * @a: first number
 * @b: second number
 * Return: remainder of two numbers
 */

int mod(int a, int b)
{
	return (a % b);
}
